							<h2>Позиція партії «ВОЛЯ» щодо реформи децентралізацї і процесу об’єднання громад.</h2>
							<p>1. Партія «ВОЛЯ» виступає за те, щоб більшість податків залишалася в місцевих бюджетах, а в громадах з’явилося реальне самоврядування, якого по суті не було за всю історію незалежності України.</p>
							<p>Тому партія «ВОЛЯ» в цілому підтримує реформу децентралізації, проте має свої застереження до деяких її положень та способу втілення.</p>
							<p>2. Реформа децентралізації, яка передбачає об’єднання громад і створення їх спільної ради, має відбуватися повністю публічно, з обговореннями в самих громадах. Цей процес не може бути кулуарним. Ми виступаємо за повну відкритість процесу об’єднання громад, за участі громадськості, участі самих громад, сільських та міських голів, бо формування Перспективного плану об’єднання має базуватися на волі громад. Тому партія «ВОЛЯ» почала масову роз’яснювальну кампанію щодо реформи, її ризиків та процесу об’єднання громад.</p>
							<p>3. Величезною загрозою об’єднання громад є те, що народ так і не стане реальним власником своєї землі, майна, природніх ресурсів, місцеві громади знову будуть обкраденими, а люди не матимуть впливу на владу і не зможуть керувати своїми місцевими справами.</p>
							<p>Тому партія «ВОЛЯ» виступає за те, щоб були прийняті Статути територіальних громад, перед об’єднанням була проведена інвентеризація майна, землі, ресурсів громади та сформований Відкритий реєстр власності громади.</p>
							<p>4. «ВОЛЯ» вимагає спрямування частини ренти з газу, нафти та інших корисних копалин, які зараз мають загальнодержавне значення, до бюджетів громад. А також, реальної фінансової децентралізації та прийняття зареєстрованих партією «ВОЛЯ» законопроектів №1898, 1899, 1900.</p>
							<p>5. «ВОЛЯ» виступає за те, щоб земля стала основним ресурсом для економічного розвитку громад, а не ресурсом для збагачення чиновників. Партія «ВОЛЯ» вимагає, щоб вся земля поза межами населених пунктів була повернута у власність громадам. А в Статутах громад були вставлені запобіжники щодо прийняття рішень про розпорядження землею, щоб така земля не була розкрадена. Для цього «ВОЛЯ» подала відповідні законопроекти до парламенту та готує типові Статути територіальних громад.</p>
