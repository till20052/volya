												<p>Однак  він не вважає, що Закон про добровільне об’єднання територіальних громад не досконалий.</p>
												<p>«Скажімо, в  Ірпінському регіоні  є селище Ворзель, яке не межує з Ірпінем,  але вони поєднані на сьогодні і адміністративно, і в історичному плані . В той же час за умовами закону про добровільне об’єднання громад формувати спільну громаду можуть тільки суміжні населені пункти.  Пропозиція від Ірпеня, міста обласного підпорядкування, надійшла і до Ворзеля.  Якщо ж громада прийме рішення про об’єднання, буде порушено одну з норм Закону», - говорить людина ВОЛІ.</p>
												<p>Щодо обізнаності населення, то воно ознайомлено дуже слабо. В цілому, про початок процесу об’днання знають влада і громадські активісти, які в силу своєї небайдужості до процесів в регіоні цікавляться поточними справами.</p>
												<p>Паралельно з активністю представників влади, які створюють робочі групи на місцях, почали працювати й ініціативні групи, що створені місцевими мешканцями та проводять незалежне опитування громадян. Оскільки населення в цілому і уяви не має про сам механізм проведення об’єднання громад, а також і про наслідки, то переважна більшість на сьогодні  надає перевагу самостійності.</p>
												<p>Ми розуміємо, якщо громади не скористаються наданим шансом на добровільне об’єднання, рано чи пізно  воно має відбутися. Тільки тоді «нарізку» об’єднаних громад доведуть «згори».</p>
