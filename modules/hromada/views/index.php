												<p>На його переконання, будь-які об’єднання, чи добровільні, чи примусові, наберуть юридичної сили, якщо кожна громада матиме, принаймні, статут, в якому будуть прописані права, обов’язки, зафіксовано об’єкти комунальної власності та стратегічні потреби мешканців того чи іншого населеного пункту, що дасть змогу захистити інтереси громади від посягань.</p>
